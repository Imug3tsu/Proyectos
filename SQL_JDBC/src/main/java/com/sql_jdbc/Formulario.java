    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try{
            con=Conexion.Conectar();
            ps=con.prepareStatement("insert into Persons(name_,surname,email,age,height,sex)value(?,?,?,?,?,?)");
            if(cbxSex.getSelectedItem().equals("Selection")){
                JOptionPane.showMessageDialog(null,"Error, sex no selected");
            }else{
                ps.setString(1,txtName.getText());
                ps.setString(2,txtSurname.getText());
                ps.setString(3,txtEmail.getText());
                ps.setInt(4,Integer.parseInt(txtAge.getText()));
                ps.setDouble(5,Double.parseDouble(txtHeight.getText()));
                ps.setString(6,cbxSex.getSelectedItem().toString());
            }
            int result=ps.executeUpdate();
            Verification(result,"Created Person succefully","Error, trying to create person");
            con.close();
        }catch(SQLException e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        try{
            con=Conexion.Conectar();
            ps=con.prepareStatement("update Persons set name_=?,surname=?,email=?,age=?,height=?,sex=? where id=?");
            if(cbxSex.getSelectedItem().equals("Selection")){
                JOptionPane.showMessageDialog(null,"Error, sex no selected");
            }else{
                ps.setString(1,txtName.getText());
                ps.setString(2,txtSurname.getText());
                ps.setString(3,txtEmail.getText());
                ps.setInt(4,Integer.parseInt(txtAge.getText()));
                ps.setDouble(5,Double.parseDouble(txtHeight.getText()));
                ps.setString(6,cbxSex.getSelectedItem().toString());
                ps.setString(7,txtId.getText());
            }
            int result=ps.executeUpdate();
            Verification(result,"Update Person succefully","Error, trying to update person");
            con.close();
        }catch(SQLException e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnModifyActionPerformed

