    public void CleanBox(){
        txtName.setText(null);
        txtSurname.setText(null);
        txtEmail.setText(null);
        txtAge.setText(null);
        txtHeight.setText(null);
        cbxSex.setSelectedIndex(0);
        txtId.setText(null);
    }
    public void Verification(int result,String great,String bad){
            if(result>0){
                JOptionPane.showMessageDialog(null,great);
                CleanBox();
            }else{
                JOptionPane.showMessageDialog(null,bad);
            }
    }
    public void Teclado(){
        try{
            con=Conexion.Conectar();
            ps=con.prepareStatement("select*from Persons where id=?");
            ps.setString(1,txtId.getText());
            rs=ps.executeQuery();
            if(rs.next()){
                txtName.setText(rs.getString("name_"));
                txtSurname.setText(rs.getString("surname"));
                txtEmail.setText(rs.getString("email"));
                txtAge.setText(rs.getString("age"));
                cbxSex.setSelectedItem(rs.getString("sex"));
                txtHeight.setText(rs.getString("height"));
            }else{
                JOptionPane.showMessageDialog(null,"person no exist!");
                CleanBox();
            }
            con.close();
        }catch(SQLException e){
            System.out.println(e.getMessage());
        }
    }

